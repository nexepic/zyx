TEST_F(IndexManagerCompositeTest, CompositeIndex_NodeDeleted) {
	// Cover removeCompositeIndexForNode via onNodeDeleted
	EXPECT_TRUE(indexManager->createCompositeIndex(
		"comp_del", "node", "Person", {"name", "age"}));

	int64_t labelId = dataManager->getOrCreateLabelId("Person");

	graph::Node n(0, labelId);
	n.addProperty("name", std::string("Dave"));
	n.addProperty("age", static_cast<int64_t>(40));
	dataManager->addNode(n);

	auto res1 = indexManager->findNodeIdsByCompositeIndex(
		{"name", "age"},
		{PropertyValue(std::string("Dave")), PropertyValue(static_cast<int64_t>(40))});
	EXPECT_GE(res1.size(), 1UL);

	// Delete node
	graph::Node toDelete = dataManager->getNode(n.getId());
	auto props = dataManager->getNodeProperties(n.getId());
	toDelete.setProperties(props);
	dataManager->deleteNode(toDelete);

	auto res2 = indexManager->findNodeIdsByCompositeIndex(
		{"name", "age"},
		{PropertyValue(std::string("Dave")), PropertyValue(static_cast<int64_t>(40))});
	EXPECT_TRUE(res2.empty());
}

