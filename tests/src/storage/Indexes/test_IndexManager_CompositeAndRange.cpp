TEST_F(IndexManagerCompositeTest, CompositeIndexUpdate_OnNodeUpdate) {
	EXPECT_TRUE(indexManager->createCompositeIndex(
		"comp_upd", "node", "Person", {"name", "age"}));

	int64_t labelId = dataManager->getOrCreateLabelId("Person");

	// Add node with both composite properties
	graph::Node n1(0, labelId);
	n1.addProperty("name", std::string("Bob"));
	n1.addProperty("age", static_cast<int64_t>(25));
	dataManager->addNode(n1);

	// Verify inserted into composite index
	auto res1 = indexManager->findNodeIdsByCompositeIndex(
		{"name", "age"},
		{PropertyValue(std::string("Bob")), PropertyValue(static_cast<int64_t>(25))});
	EXPECT_GE(res1.size(), 1UL);

	// Update the node (triggers removeCompositeIndexForNode + updateCompositeIndexForNode)
	graph::Node n2(n1.getId(), labelId);
	n2.addProperty("name", std::string("Bob"));
	n2.addProperty("age", static_cast<int64_t>(26));
	indexManager->onNodeUpdated(n1, n2);

	// Old composite entry should be gone
	auto resOld = indexManager->findNodeIdsByCompositeIndex(
		{"name", "age"},
		{PropertyValue(std::string("Bob")), PropertyValue(static_cast<int64_t>(25))});
	EXPECT_TRUE(resOld.empty());

	// New composite entry should exist
	auto resNew = indexManager->findNodeIdsByCompositeIndex(
		{"name", "age"},
		{PropertyValue(std::string("Bob")), PropertyValue(static_cast<int64_t>(26))});
	EXPECT_GE(resNew.size(), 1UL);
}

